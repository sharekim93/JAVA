
public class Q003 {

	public static void main(String[] args) {
		
		/* 다음의 문장에서 리터럴 변수 상수 키워드를 적으시오
		 * int i = 100;
		 * long l = 1000L;
		 * final float PI = 3.14f;
		 *  - 리터럴
		 *  - 변수
		 *  - 키워드
		 *  - 상수
		 */
		
		System.out.println("다음의 문장에서 리터럴 변수 상수 키워드를 적으시오");
		System.out.println("int i = 100;");
		System.out.println("long l = 1000L;");
		System.out.println("final float PI = 3.14f;\n"); //final이 붙으면 상수
		System.out.println(" - 리터럴 : 100, 1000L, 3.14f");
		System.out.println(" - 변수 : i, l");
		System.out.println(" - 키워드 : int, long, final, float");
		System.out.println(" - 상수 : PI");
	}
}
