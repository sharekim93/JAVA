
public class Ex008 {

	public static void main(String[] args) {
		//[GIGO] 변수, 입력, 처리, 출력
		//변수
		int a;
		int b;
		//입력
		a=10;
		b=3;
		//처리 + 출력
		System.out.println(a + "+" + b + "=" + (a+b));
		System.out.println(a + "-" + b + "=" + (a-b));
		System.out.println(a + "*" + b + "=" + (a*b));
		System.out.println(a + "/" + b + "=" + (a/b));
		System.out.println("a/b 가 3.33이 아닐 때 해결방법 : a또는 b의 자료형을 변경해야 합니다.");
	}

}
